def divide(x):
    # Takes a numeric parameter from the user, divides it by 2 and then returns the result.
    # param x numeric
    # returns x/2
    return x / 2

def mult(x):
    # Takes a numeric parameter from the user, multiplies it by 4 and then returns the result.
    # param x numeric
    # returns x * 4
    return x * 4

x = divide(5)

print(x)

print(mult(x))